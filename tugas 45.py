print("##==Jumlah Kelereng Aldi==##")
Kelereng_Aldi=int(input("Masukkan Jumlah Kelereng Aldi :"))

print("##==Menghitung Jumlah Kelereng Budi==##")
Kelereng_Budi=Kelereng_Aldi-15
print("Kelereng_Budi :", Kelereng_Budi)

print("##==Menghitung Jumlah Kelereng Anto==##")
Kelereng_Anto=2*(Kelereng_Aldi+Kelereng_Budi)
print("Kelereng_Anto",Kelereng_Anto)

print("##==Menghitung Jumlah Kelereng Agung==##")
Kelereng_Agung=(Kelereng_Aldi+Kelereng_Budi+Kelereng_Anto)-5
print("Kelereng_Agung",Kelereng_Agung)