# Masukkan jumlah kelereng Aldi
Aldi = int(input("Masukkan jumlah kelereng Aldi: "))

# Hitung jumlah kelereng Budi
Budi = Aldi - 15
print(f"Jumlah Kelereng Budi :{Budi}")

# Hitung jumlah kelereng Anto
Anto = 2 * (Aldi + Budi)
print(f"Jumlah Kelereng Anto :{Anto}")

# Hitung jumlah kelereng Agung
Agung = (Aldi + Budi + Anto) - 5
print(f"Jumlah Kelereng Agung :{Agung}")
